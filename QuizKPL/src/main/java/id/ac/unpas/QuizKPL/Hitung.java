package id.ac.unpas.QuizKPL;

public class Hitung {
	public int hitungTambah(int num1, int num2) {
		int hasil = num1+ num2;
		return hasil;
	}
	public int hitungKurang(int num1, int num2) {
		int hasil = num1 - num2;
		return hasil;
	}

	public int hitungBagi(int num1, int num2) {
		int hasil = num1 / num2;
		return hasil;
	}
	public int hitungKali(int num1, int num2) {
		int hasil = num1 * num2;
		return hasil;
	}
}
